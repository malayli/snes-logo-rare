/*!\brief Initialize the Rare logo screen in mode 7.
*/
void initRareLogoMode7() {
    // Tilemap addresses
    // BG1 = 0x7400
    // BG2 = 0x7800
    // BG3 = 0x7000

    // Tileset addresses
    // BG1 = 0x4000
    // BG2 = 0x6000
    // BG3 = 0x3000

    // Now Put mode7 without anything else
    setMode7(0);

    bgSetScroll(0, 0, 0);

    // Enable large tilemap
    REG_M7SEL = 0b10000000;

    // Set mode 7 map scale
    logoScale = 256;
    REG_M7A = logoScale;
    REG_M7A = logoScale>>8;
    REG_M7D = logoScale;
    REG_M7D = logoScale>>8;

    // Set mode 7 map {x,y} position
    sx = 261;
    sy = 215;
    REG_M7X = (sx) & 255;
    REG_M7X = (sx) >> 8;
    REG_M7Y = (sy) & 255;
    REG_M7Y = (sy) >> 8;
}

/*!\brief Initialize the Rare logo screen.
*/
void initRareLogo() {
    logoState = 0;
    framesCounter = 0;

    setBrightness(0); 

    // Load the mode 7 logo tileset for loading time optimization
    bgInitMapTileSet7(
        &logoMode7Pic, 
        &logoMode7TileMap, 
        &logoMode7Palette, 
        (&logoMode7Pic_end - &logoMode7Pic), 
        0x0000);

    initRareLogoMode3();

    WaitForVBlank();
    initLogoMusic();
    
    WaitForVBlank();
    spcPlay(0);
    spcProcess();
    WaitForVBlank();

    setBrightness(0xF);
}

/*!\brief Update Rare logo animation.
    \return 1 when the logo animation is complete, 0 otherwise.
*/
u8 updateRareLogo() {
    // Frame 317 : load mode7
    // Frame 367 : load mode5

    switch(logoState) {
        case 0:
            if (framesCounter == 64) {
                logoState = 1;
                initRareLogoMode7();
                dmaCopyCGram(&logoMode7Palette, PAL0, 32*2);
            }
            break;

        case 1:
            if (logoScale < 880) {
                // Mode 7 zoom out

                logoScale += 8;

                REG_M7A = logoScale; // Set the value in 1st byte
                REG_M7A = logoScale>>8; // Set the value in 2nd byte

                REG_M7D = logoScale; // Set the value in 1st byte
                REG_M7D = logoScale>>8; // Set the value in 2nd byte

            } else {
                // Load mode 5 screen

                // Tilemap addresses
                // BG1 = 0x7400
                // BG2 = 0x7800
                // BG3 = 0x7000

                // Tileset addresses
                // BG1 = 0x4000
                // BG2 = 0x6000
                // BG3 = 0x3000
                
                // Load the mode 5 logo tileset for loading time optimization
                bgInitTileSet(BG0, 
                    &logoMode5Pic, 
                    &logoMode5Palette, 
                    PAL0, 
                    (&logoMode5Pic_end - &logoMode5Pic), 
                    (&logoMode5Palette_end - &logoMode5Palette), 
                    BG_16COLORS, 
                    0x2000);

                WaitForVBlank();

                logoState = 2;
                REG_BGMODE = BG_MODE5;
                REG_CGWSEL = 0b00000000;
                REG_TM = 0b00000001;
                REG_TS = 0b00000001;
                bgSetGfxPtr(BG0, 0x2000);
                bgSetMapPtr(BG0, 0x7400, SC_32x32);
                dmaCopyVram((u8 *)logoMode5TileMap, 0x7400, 32*32*2);
                dmaCopyCGram(&logoMode5Palette, PAL0, 32*7);

                // Force VBlank
                REG_INIDISP = 0b00001111;
            }
            break;
        
        case 2:
            if (framesCounter == 300) {
                return 1;
            }
            break;
    }

    framesCounter++;

    return 0;
}
